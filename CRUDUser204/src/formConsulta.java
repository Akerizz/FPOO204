    private void btsBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsBuscarActionPerformed
        // TODO add your handling code here:
        //obtener el valor de txt
        String idtext = txtId.getText();
        //validar txt vacio
        if(idtext.isEmpty()){
        JOptionPane.showMessageDialog(this,"El id es obigatorio","Error",JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        try{
        int Id = Integer.parseInt(idtext);
        ResultSet rs = crud.buscarPorID(Id);
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setRowCount(0);
        
        if(rs.next()){
        modelo.addRow(new Object[]{rs.getInt("id"),rs.getString("Nombre"),rs.getString("Correo"),rs.getString("Contrasena")});
        }
        }
        catch(SQLException e){
            System.out.println("Error al llenar la tabla"+  e.getMessage());
        }
        
    }//GEN-LAST:event_btsBuscarActionPerformed

    private void btsTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsTodosActionPerformed
        // TODO add your handling code here:
        ResultSet todos = crud.obteneTodos();
        DefaultTableModel modeloT = (DefaultTableModel) jTable1.getModel();
        modeloT.setRowCount(0);
        
        try{
            while(todos.next()){
                    modeloT.addRow(new Object[]{todos.getInt("id"),todos.getString("Nombre"),todos.getString("Correo"),todos.getString("Contrasena")});
            }
        }
        catch(SQLException x){
            System.out.println("Error al mostrar todos"+ x.getMessage());
        }
        
    }//GEN-LAST:event_btsTodosActionPerformed

